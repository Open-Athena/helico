"""Numerical-regression tests against golden snapshots in tests/data/snapshots/.

Purpose: catch unintended numerical drift in refactors. Each test loads a
snapshot (generated by scripts/pm/regenerate_snapshots.py from the
Protenix v1.0.0 weights with a fixed seed) and re-runs the same path
under the current code. If output changes beyond bf16 tolerance, the test
fails — the refactor (or model edit) is changing numerics.

If a change is intentional, regenerate with:
    uv run python scripts/pm/regenerate_snapshots.py --force
and commit the .npz diff alongside the code change.

Requires:
    - CUDA
    - checkpoints/protenix_base_default_v1.0.0.pt
    - tests/data/snapshots/*.npz (committed)
"""

