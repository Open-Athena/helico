class TestDiffusionSnapshot:
    """Pin final diffusion coords (5 EDM steps, seeded trunk+noise)."""

    @pytest.fixture(scope="class")
    def snapshot(self):
        path = SNAPSHOTS_DIR / "diffusion.npz"
        assert path.exists(), f"Regenerate with scripts/pm/regenerate_snapshots.py"
        return dict(np.load(path))

    @pytest.fixture(scope="class")
    def actual(self, protenix_model, canonical_batch):
        model = protenix_model
        batch = canonical_batch

        orig_steps = model.diffusion.n_steps
        model.diffusion.n_steps = 5
        try:
            torch.manual_seed(SEED)
            with torch.no_grad(), torch.amp.autocast("cuda", dtype=torch.bfloat16):
                mask = batch.get("token_mask")
                pair_mask = None
                if mask is not None:
                    pair_mask = (mask.unsqueeze(-1) & mask.unsqueeze(-2)).float()

                ref_charge, ref_features = build_ref_features(batch)
                atom_mask = batch.get("atom_mask",
                                       torch.ones(1, batch["atom_coords"].shape[1], device=DEVICE))
                atom_mask = atom_mask.float()
                s_inputs = build_s_inputs(model.input_embedder, batch, ref_charge, ref_features, atom_mask)
                s_init = model.linear_sinit(s_inputs)
                z_init = (
                    model.linear_zinit1(s_init).unsqueeze(2)
                    + model.linear_zinit2(s_init).unsqueeze(1)
                )
                relpe_feats = build_relpe_feats(batch)
                z_init = z_init + model.trunk_relpe(**relpe_feats)
                msa_raw, msa_mask = build_msa_raw(batch)

                s = torch.zeros_like(s_init)
                z = torch.zeros_like(z_init)
                for _ in range(2):
                    z = z_init + model.linear_z_cycle(model.layernorm_z_cycle(z))
                    z = z + model.template_embedder(batch, z)
                    z = model.msa_module(msa_raw, z, s_inputs, msa_mask, pair_mask,
                                          msa_chunk_size=2048)
                    s = s_init + model.linear_s(model.layernorm_s(s))
                    s, z = model.pairformer(s, z, mask=mask, pair_mask=pair_mask)

                torch.manual_seed(SEED)  # fresh for diffusion noise
                coords = model.diffusion.sample(
                    ref_pos=batch["ref_coords"],
                    ref_charge=ref_charge,
                    ref_features=ref_features,
                    atom_to_token=batch["atom_to_token"],
                    atom_mask=atom_mask,
                    s_trunk=s, z_trunk=z, s_inputs=s_inputs,
                    relpe_feats=relpe_feats,
                    ref_space_uid=batch.get("ref_space_uid"),
                )
        finally:
            model.diffusion.n_steps = orig_steps
        return {"coords": coords}

    def test_coords_statistics(self, snapshot, actual):
        """Diffusion inherits cuDNN non-determinism (two identical runs can
        drift by tens of Å in atom coords). Bit-for-bit comparison is
        unrealistic; instead we pin distributional stats — mean, std,
        per-atom radius. A genuine refactor bug would shift these.
        """
        a = actual["coords"].detach().to(dtype=torch.float32, device="cpu").numpy()
        e = snapshot["coords"]
        assert a.shape == e.shape
        # Compare statistics. Tolerances are generous because stochastic
        # coords vary run-to-run; a real numerical regression would typically
        # move these metrics by >50%.
        for name, a_val, e_val in [
            ("mean",    a.mean(),              e.mean()),
            ("std",     a.std(),               e.std()),
            ("abs_max", np.abs(a).max(),       np.abs(e).max()),
            ("centre_x", a.reshape(-1, 3).mean(0)[0],
                         e.reshape(-1, 3).mean(0)[0]),
            ("centre_y", a.reshape(-1, 3).mean(0)[1],
                         e.reshape(-1, 3).mean(0)[1]),
            ("centre_z", a.reshape(-1, 3).mean(0)[2],
                         e.reshape(-1, 3).mean(0)[2]),
        ]:
            scale = max(abs(e_val), 1.0)
            rel = abs(a_val - e_val) / scale
            assert rel < 0.5, (
                f"diffusion {name}: actual {a_val:.3f} vs snapshot {e_val:.3f} "
                f"(relative diff {rel:.3f} > 0.5 threshold)"
            )


