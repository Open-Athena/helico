class TestFold1MBNFast:
    """Fold 1MBN with aggressive speed settings (1 sample, 3 cycles, 20
    diffusion steps) and assert looser correctness thresholds than
    TestFoldRealProtein. Takes ~30-60 sec instead of 7 min, so it can run
    on every refactor commit.

    Purpose: catch end-to-end regressions the trunk snapshot can't see —
    e.g. confidence-head numerics, diffusion-module wiring, post-processing.
    Not meant as a correctness proof; that's what the full-quality
    TestFoldRealProtein in test_model.py provides.
    """

    @pytest.fixture(scope="class")
    def model_fast(self):
        ckpt = torch.load(CHECKPOINT, map_location="cpu", weights_only=False)
        sd = ckpt["model"]
        sd = OrderedDict((k.removeprefix("module."), v) for k, v in sd.items())
        model = Helico(HelicoConfig())
        load_protenix_state_dict(sd, model)
        # Shrink diffusion step count at the module level
        model.diffusion.n_steps = 20
        return model.to(device=DEVICE).eval()

    @pytest.fixture(scope="class")
    def metrics(self, model_fast):
        """Run predict_target with fast settings; return CA RMSD, pLDDT, pTM."""
        from helico.data import parse_ccd, parse_mmcif, parse_a3m, a3m_to_msa_matrix, compute_msa_features
        from helico.bench import (
            structure_to_chains, predict_target, match_atoms,
            extract_backbone_coords, compute_rmsd,
        )

        assert CIF_PATH.exists(), f"Missing {CIF_PATH}"
        assert A3M_PATH.exists(), f"Missing {A3M_PATH}"

        ccd = parse_ccd(cache_path=CCD_CACHE if CCD_CACHE.exists() else None)
        gt_structure = parse_mmcif(CIF_PATH)
        seqs, _ = parse_a3m(A3M_PATH.read_text())
        msa, dels = a3m_to_msa_matrix(seqs)
        msa_feat = compute_msa_features(msa, dels)
        chains = structure_to_chains(gt_structure)

        torch.manual_seed(SEED)
        out = predict_target(
            model_fast, chains, ccd,
            msa_features=msa_feat,
            n_samples=1, device=DEVICE, dtype=torch.bfloat16, n_cycles=3,
        )
        assert out is not None, "predict_target returned None"
        tokenized, results = out

        pred_coords = results["coords"][0].cpu().float().numpy()
        matched = match_atoms(tokenized, pred_coords, gt_structure)
        pred_bb, gt_bb, _ = extract_backbone_coords(matched)
        return {
            "ca_rmsd": compute_rmsd(pred_bb, gt_bb),
            "mean_plddt": results["plddt"][0].mean().item(),
            "ptm": results["ptm"][0].item(),
            "n_backbone_atoms": len(pred_bb),
        }

    def test_ca_rmsd_loose(self, metrics):
        """CA RMSD loose threshold. Full test in test_model.py uses 1.0 Å
        with 10 samples; here we allow 4.0 Å since we're using 1 sample,
        3 cycles, 20 diffusion steps."""
        assert metrics["ca_rmsd"] < 4.0, (
            f"CA RMSD {metrics['ca_rmsd']:.2f} Å exceeds 4.0 Å — "
            f"end-to-end pipeline may have regressed"
        )

    def test_plddt_reasonable(self, metrics):
        """Mean pLDDT > 60 (full test uses 80)."""
        assert metrics["mean_plddt"] > 60.0, (
            f"Mean pLDDT {metrics['mean_plddt']:.1f} < 60 — "
            f"model is very unconfident; confidence head may have regressed"
        )

    def test_ptm_in_range(self, metrics):
        """pTM in [0, 1]."""
        assert 0.0 <= metrics["ptm"] <= 1.0, f"pTM {metrics['ptm']:.3f} outside [0, 1]"

    def test_matched_ca_atoms(self, metrics):
        """1MBN = 153 residues → extract_backbone_coords returns 153 CAs.
        Any number substantially below 150 means tokenization or atom
        matching has regressed."""
        assert metrics["n_backbone_atoms"] > 140, (
            f"Only {metrics['n_backbone_atoms']} CA atoms matched — "
            f"expected ~153 for 1MBN"
        )
