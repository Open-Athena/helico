import numpy as np
import pytest
import torch

REPO = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO / "src"))

from helico.data import make_synthetic_batch
from helico.model import Helico, HelicoConfig
from helico.model.features import (
    build_ref_features,
    build_relpe_feats,
    build_s_inputs,
    build_msa_raw,
)
from helico.load_protenix import load_protenix_state_dict

SNAPSHOTS_DIR = Path(__file__).resolve().parent / "data" / "snapshots"
CHECKPOINT = REPO / "checkpoints" / "protenix_base_default_v1.0.0.pt"
TESTS_DATA = Path(__file__).resolve().parent / "data"
CIF_PATH = TESTS_DATA / "1mbn.cif.gz"
A3M_PATH = TESTS_DATA / "1mbn.a3m"
CCD_CACHE = REPO / "data" / "ccd_cache.pkl"
if not CCD_CACHE.exists():
    from helico.data import _processed_dir
    CCD_CACHE = _processed_dir() / "ccd_cache.pkl"
SEED = 42
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

# Tolerance rationale: bf16 has ~8 mantissa bits → ~3.9e-3 relative error
# compounds through dozens of ops. Pre-recycle tensors are close enough
# across GPUs that per-element atol/rtol works. Post-recycle values
# accumulate bf16 noise across 2+ recycle iterations and the noise
# varies per GPU generation (A5000 vs H100 etc.), so we use a
# whole-tensor rel_L2 check instead — robust to cross-GPU noise but
# still catches refactor drift (which would change numerics
# coherently, not just at the edges).
RTOL = 1e-2
ATOL = 5e-2
REL_L2_TOL = 5e-2   # Whole-tensor ‖Δ‖ / ‖expected‖ — used for post-recycle

_PROTENIX_AVAIL = CHECKPOINT.exists() and DEVICE == "cuda"
pytestmark = [
    pytest.mark.numerics,
    pytest.mark.skipif(
        not _PROTENIX_AVAIL,
        reason=f"Requires CUDA + {CHECKPOINT.name}",
    ),
]


