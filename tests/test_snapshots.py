class TestTrunkSnapshot:
    """Pin trunk activations (s_inputs, s_init, z_init, post-recycle s/z)
    with Protenix weights + seeded synthetic input. Detects any refactor
    that changes trunk numerics."""

    @pytest.fixture(scope="class")
    def snapshot(self):
        path = SNAPSHOTS_DIR / "trunk.npz"
        assert path.exists(), f"Regenerate with scripts/pm/regenerate_snapshots.py"
        return dict(np.load(path))

    @pytest.fixture(scope="class")
    def actual(self, protenix_model, canonical_batch):
        """Run the trunk path end-to-end, return captured tensors."""
        model = protenix_model
        batch = canonical_batch

        torch.manual_seed(SEED)
        with torch.no_grad(), torch.amp.autocast("cuda", dtype=torch.bfloat16):
            mask = batch.get("token_mask")
            pair_mask = None
            if mask is not None:
                pair_mask = (mask.unsqueeze(-1) & mask.unsqueeze(-2)).float()

            ref_charge, ref_features = build_ref_features(batch)
            atom_mask = batch.get("atom_mask",
                                   torch.ones(1, batch["atom_coords"].shape[1], device=DEVICE))
            atom_mask = atom_mask.float()
            s_inputs = build_s_inputs(model.input_embedder, batch, ref_charge, ref_features, atom_mask)
            s_init = model.linear_sinit(s_inputs)
            z_init = (
                model.linear_zinit1(s_init).unsqueeze(2)
                + model.linear_zinit2(s_init).unsqueeze(1)
            )
            relpe_feats = build_relpe_feats(batch)
            z_init = z_init + model.trunk_relpe(**relpe_feats)
            msa_raw, msa_mask = build_msa_raw(batch)

            s = torch.zeros_like(s_init)
            z = torch.zeros_like(z_init)
            for _ in range(2):  # matches snapshot script N_CYCLES=2
                z = z_init + model.linear_z_cycle(model.layernorm_z_cycle(z))
                z = z + model.template_embedder(batch, z)
                z = model.msa_module(msa_raw, z, s_inputs, msa_mask, pair_mask,
                                      msa_chunk_size=2048)
                s = s_init + model.linear_s(model.layernorm_s(s))
                s, z = model.pairformer(s, z, mask=mask, pair_mask=pair_mask)

        return {
            "s_inputs": s_inputs, "s_init": s_init, "z_init": z_init,
            "s_post_recycle": s, "z_post_recycle": z,
            "ref_charge": ref_charge, "ref_features": ref_features,
            "msa_raw": msa_raw,
        }

    def test_s_inputs(self, snapshot, actual):
        _assert_matches(actual["s_inputs"], snapshot["s_inputs"], "s_inputs")

    def test_s_init(self, snapshot, actual):
        _assert_matches(actual["s_init"], snapshot["s_init"], "s_init")

    def test_z_init(self, snapshot, actual):
        _assert_matches(actual["z_init"], snapshot["z_init"], "z_init")

    def test_s_post_recycle(self, snapshot, actual):
        # Post-recycle tensors accumulate bf16 noise across 2 recycle
        # iterations; noise magnitude varies per-GPU. Use rel_L2.
        _assert_matches_rel_l2(actual["s_post_recycle"], snapshot["s_post_recycle"],
                                "s_post_recycle")

    def test_z_post_recycle(self, snapshot, actual):
        _assert_matches_rel_l2(actual["z_post_recycle"], snapshot["z_post_recycle"],
                                "z_post_recycle")

    def test_ref_charge(self, snapshot, actual):
        _assert_matches(actual["ref_charge"], snapshot["ref_charge"], "ref_charge")

    def test_ref_features(self, snapshot, actual):
        _assert_matches(actual["ref_features"], snapshot["ref_features"],
                        "ref_features")

    def test_msa_raw(self, snapshot, actual):
        _assert_matches(actual["msa_raw"], snapshot["msa_raw"], "msa_raw")


